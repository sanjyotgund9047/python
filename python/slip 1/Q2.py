# Define two sets
set1 = {1, 2, 3, 4, 5}
set2 = {4, 5, 6, 7, 8}

# Set difference
diff_set = set1 - set2
print("Set Difference:", diff_set)

# Set union
union_set = set1 | set2
print("Set Union:", union_set)

# Set intersection
intersect_set = set1 & set2
print("Set Intersection:", intersect_set)

# Set symmetric difference
sym_diff_set = set1 ^ set2
print("Set Symmetric Difference:", sym_diff_set)
